#include "cub3d.h"

float   degToRad(float a)
{
	return (a * (float)M_PI / 180.0);
}

float FixAng(float a)
{ 
    if(a > 180)
        a -= 360;
    if(a <= -180)
        a += 360;
    return (a);
}